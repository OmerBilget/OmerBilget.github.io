function create_rocket_enemy(enemy,direction,rocket_array,type,target,pool){
    var rocket=pool.getrocket_enemy();
    var rocket_coordinate=rocket.translation;
    var rocket_direction=rocket.direction;
    vec3.copy(rocket_direction,direction);
    vec3.copy(rocket_coordinate,enemy.translation);
    
    
   
    rocket.generate(rocket_coordinate,rocket_direction,1,0,0,700,type,target);
    rocket_array[active_length_enemy_rocket]=rocket;
    active_length_enemy_rocket+=1;
}


function collision_enemy_rocket(rocket_array,player){
    let len=active_length_enemy_rocket;
    if(len==0){
        return;
    }
    for(let i=0;i<len;i++){
        if(rocket_array[i].life<=0){
            continue;
        }
        var dist=vec3.distance(rocket_array[i].translation,player.coordinate);
        if(dist<5){
            player.damage_player(20);
            if(game.state!=1){
                return;
            }
            rocket_array[i].life=0;
        }
    }
}

