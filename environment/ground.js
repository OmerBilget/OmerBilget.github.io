//these values based on look experimental
var gradient_array=[0,5,20,30,32,50,60,70,67,90,100,100];

function get_gradient(gradient_array,val){
    var index=Math.floor(val*10);
    if(index==gradient_array.length-1){
        return gradient_array[index]/100;
    }
    var min=Math.min(gradient_array[index],gradient_array[index+1]);
    var value=min;
    return value/100;
}

function normalize(val,min,max){
    return(val-min)/(max-min);
}
function createBufferArrayGround(object) {
    var bufferArray = [];
    var m=find_min_max(1,object.vertices);
    for (var i = 0; i < object.faces.length; i++) {
        for (var j = 0; j < 3; j++) {
            var tmp = object.faces[i][j][0];
            for (var k = 0; k < 3; k++) {

                bufferArray.push(object.vertices[tmp][k]);

            }
            var color=getcolor2(object.vertices[tmp][1],m[0],m[1],object.horizoncolor,object.polarcolor);
            var r=color[0];
            var g=color[1];
            var b=color[2];
            bufferArray.push(r, g, b);
        }

    }
    return bufferArray;
}

//find min and max elevation
function find_min_max(index,vertices){
    var min=10000;
    var max=-10000;
    for(var i=0;i<vertices.length;i++){
        if(vertices[i][index]<min){
            min=vertices[i][index];
        }
        if(vertices[i][index]>max){
            max=vertices[i][index];
        }
    }
    return [min,max];
}


function getcolor2(val,min,max,mincolor,maxcolor){
    var normalize=(val-min)/(max-min);
    var color=[0,0,0];
    for(let i=0;i<3;i++){
        const difference=maxcolor[i]-mincolor[i];
        const colorval=mincolor[i]+difference*normalize;
        color[i]=get_gradient(gradient_array,colorval);
    }
    return color;
}


