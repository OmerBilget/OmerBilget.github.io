class MOON{
    vertices=[];
    faces=[];
    arraybuffer=[];
    normal=[];
    normalbuffer=[];
    buffer;
    bufferNormal;
    program;
    angle;
    orbit_radius;
    orbit_speed;
    center;
    rotation_speed;
    rotation_angle;
    cycle;
    constructor(translation,rotation,scale,color,planet,angle,orbit_speed,orbit_radius,rotation_speed){
        this.translation=translation;
        this.rotation=rotation;
        this.scale=scale;
        this.color=color;
        this.planet=planet;
        parseOBJvf(moonOBJ,this,this.scale);
        this.arraybuffer=new Float32Array(createBufferColor(this,this.color));
        this.normal=calculate_normals(this);
        this.normalbuffer=new Float32Array(createNormalBuffer(this));
        this.angle=angle;
        this.orbit_speed=orbit_speed;
        this.orbit_radius=orbit_radius;
        this.rotation_speed=rotation_speed;
        this.rotation_angle=0;
        this.cycle=false;
    }

    orbit(center){
        var a=[0,0,-1];
        vec3.scale(a,a,this.orbit_radius);
        vec3.rotateY(a,a,origin_vec,moon.angle);
        vec3.rotateX(a,a,origin_vec,deg2rad(-70));
        //vec3.rotateZ(a,a,[0,0,0],deg2rad(30));
        
        vec3.add(this.translation,center,a);
        moon.angle+=moon.orbit_speed;
        if(moon.translation[1]<0 && this.cycle==false){
            moon.angle+=Math.PI*0.98;
            this.cycle=true;
        }else if(moon.translation[1]>=0 && this.cycle==true){
            this.cycle=false;
        }
        moon.angle=moon.angle%(Math.PI*2);
   }
    createBufferData(gl){
        this.buffer=gl.createBuffer();
        gl.bindBuffer(gl.ARRAY_BUFFER,this.buffer);
        gl.bufferData(gl.ARRAY_BUFFER,this.arraybuffer,gl.STATIC_DRAW);

        this.bufferNormal=gl.createBuffer();
        gl.bindBuffer(gl.ARRAY_BUFFER,this.bufferNormal);
        gl.bufferData(gl.ARRAY_BUFFER,this.normalbuffer,gl.STATIC_DRAW);
    }
}


function draw_moon(gl,moon,light_source,program,identitymatrix,w,v,p,n,world,view,projection,normal,pos,color,normalLocation,lightDirection,lightColor,lightlocation,d){
  
   

    gl.useProgram(program);
    gl.bindBuffer(gl.ARRAY_BUFFER,moon.buffer);
    gl.vertexAttribPointer(pos,3,gl.FLOAT,gl.FALSE,6*Float32Array.BYTES_PER_ELEMENT,0);
    gl.vertexAttribPointer(color,3,gl.FLOAT,gl.FALSE,6*Float32Array.BYTES_PER_ELEMENT,3*Float32Array.BYTES_PER_ELEMENT);
    gl.enableVertexAttribArray(pos);
    gl.enableVertexAttribArray(color);



    gl.bindBuffer(gl.ARRAY_BUFFER,moon.bufferNormal);
    gl.vertexAttribPointer(normalLocation,3,gl.FLOAT,gl.FALSE,3*Float32Array.BYTES_PER_ELEMENT,0);
    gl.enableVertexAttribArray(normalLocation);
    

    mat4.translate(w,identitymatrix,moon.translation);
    mat4.targetTo(w,moon.translation,player.coordinate,direction_up);
    
    
    vec3.sub(tmp_vector,origin_vec,moon.translation);
    vec3.cross(tmp_vector,direction_down,tmp_vector);
    mat4.rotate(w,w,moon.rotation_angle,tmp_vector);
    //mat4.rotateZ(w,w,moon.rotation_angle);
    //mat4.rotateY(w,w,deg2rad(0));
   
    
    moon.rotation_angle+=moon.rotation_speed;
    vec3.add(player.camera_coordinates,player.coordinate,player.lookingto);
    glMatrix.mat4.lookAt(v,player.coordinate,player.camera_coordinates,direction_up);//camera 

    gl.uniformMatrix4fv(world,gl.FALSE,w);
    gl.uniformMatrix4fv(view,gl.FALSE,v);
    gl.uniformMatrix4fv(projection,gl.FALSE,p);


   
    vec3.sub(tmp_vector,light_source.translation,moon.translation);
   
    vec3.normalize(tmp_vector,tmp_vector);
    gl.uniform3fv(lightDirection,tmp_vector);


    gl.uniform3fv(lightColor,moon_light);
    var n=mat4.create();
   
    
   
    gl.uniform3fv(lightlocation,light_source.translation);
   
    
    var dist=vec3.distance(moon.translation,light_source.translation);
    gl.uniform1f(d,dist);
   

    mat4.identity(n);
    mat4.mul(n,w,n);
    mat4.invert(n,n);
    mat4.transpose(n,n);
    gl.uniformMatrix4fv(normal,gl.FALSE,n);

    gl.drawArrays(gl.TRIANGLES,0,moon.arraybuffer.length/6);
}

