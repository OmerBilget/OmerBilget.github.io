class PLANET{
    vertices=[];
    faces=[];
    arraybuffer=[];
    normal=[];
    normalbuffer=[];
    buffer;
    bufferNormal;
    material=[];
    materialindex=[];
    materialid=[];
    program;
    angle;

    constructor(translation,rotation,scale){
        this.translation=translation;
        this.rotation=rotation;
        this.scale=scale;
        parseOBJ(planetOBJ,this,this.scale);
        parseMTL(planetMTL,this);
        this.arraybuffer=new Float32Array(createBufferArray(this));
        this.normal=calculate_normals(this);
        this.normalbuffer=new Float32Array(createNormalBuffer(this));
        this.angle=0;
    }
    createBufferData(gl){
        this.buffer=gl.createBuffer();
        gl.bindBuffer(gl.ARRAY_BUFFER,this.buffer);
        gl.bufferData(gl.ARRAY_BUFFER,this.arraybuffer,gl.STATIC_DRAW);

        this.bufferNormal=gl.createBuffer();
        gl.bindBuffer(gl.ARRAY_BUFFER,this.bufferNormal);
        gl.bufferData(gl.ARRAY_BUFFER,this.normalbuffer,gl.STATIC_DRAW);
     }
}


function draw_planet(gl,planet,light_source,program,identitymatrix,w,v,p,n,world,view,projection,normalMatrix,pos,color,lightDirection,lightColor,normalLocation){
   
    gl.useProgram(program);
    gl.bindBuffer(gl.ARRAY_BUFFER,planet.buffer);
    
    gl.vertexAttribPointer(pos,3,gl.FLOAT,gl.FALSE,6*Float32Array.BYTES_PER_ELEMENT,0);
    gl.vertexAttribPointer(color,3,gl.FLOAT,gl.FALSE,6*Float32Array.BYTES_PER_ELEMENT,3*Float32Array.BYTES_PER_ELEMENT);
    gl.enableVertexAttribArray(pos);
    gl.enableVertexAttribArray(color);


    
    gl.bindBuffer(gl.ARRAY_BUFFER,planet.bufferNormal);

    gl.vertexAttribPointer(normalLocation,3,gl.FLOAT,gl.FALSE,3*Float32Array.BYTES_PER_ELEMENT,0);
    gl.enableVertexAttribArray(normalLocation);
    

    mat4.translate(w,identitymatrix,planet.translation);
    mat4.targetTo(w,planet.translation,player.coordinate,direction_up);
    
    mat4.rotateX(w,w,deg2rad(planet.rotation[0]));
    mat4.rotateY(w,w,deg2rad(planet.rotation[1])+planet.angle);
    mat4.rotateZ(w,w,deg2rad(planet.rotation[2]));
    planet.angle+=0.002;
    planet.angle%=Math.PI*2;
    vec3.add(player.camera_coordinates,player.coordinate,player.lookingto);
    glMatrix.mat4.lookAt(v,player.coordinate,player.camera_coordinates,direction_up);//camera 

    gl.uniformMatrix4fv(world,gl.FALSE,w);
    gl.uniformMatrix4fv(view,gl.FALSE,v);
    gl.uniformMatrix4fv(projection,gl.FALSE,p);


    
    vec3.sub(tmp_vector,light_source.translation,planet.translation);
    vec3.normalize(tmp_vector,tmp_vector);
    gl.uniform3fv(lightDirection,tmp_vector);
   
    gl.uniform3fv(lightColor,light_source.color);
    let n_planet=mat4.create();
    mat4.multiply(n_planet,v,w);
    mat4.invert(n_planet,n_planet);
    mat4.transpose(n_planet,n_planet);
    gl.uniformMatrix4fv(normalMatrix,gl.FALSE,n);

    gl.drawArrays(gl.TRIANGLES,0,planet.arraybuffer.length/6);
}




