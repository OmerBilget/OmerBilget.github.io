class SNIPER_RIFLE extends GUN{
    arraybufferReload_1=[];
    arraybufferReload_2=[];
    arraybufferReload_3=[];
    arraybufferReload_4=[];
    bufferReload_1;
    bufferReload_2;
    bufferReload_3;
    bufferReload_4;
    recoil;
    recoil_angle;
    recoil_time;
    t;
   constructor(translation,rotation,scale,gun_location,id,damage,cooldown,max_ammo,ammo_life,ammo_speed,ammo_create_location,spread,objText,mtlText){
        super(translation,rotation,scale,gun_location,id,damage,cooldown,max_ammo,ammo_life,ammo_speed,ammo_create_location,spread,objText,mtlText);
        this.material=[];
        parseMTL(sniperMTL_reload_1,this);
        this.arraybufferReload_1=new Float32Array(createBufferArray(this));
        this.material=[];
        parseMTL(sniperMTL_reload_2,this);
        this.arraybufferReload_2=new Float32Array(createBufferArray(this));
        this.material=[];
        parseMTL(sniperMTL_reload_3,this);
        this.arraybufferReload_3=new Float32Array(createBufferArray(this));
        this.material=[];
        parseMTL(sniperMTL_reload_4,this);
        this.arraybufferReload_4=new Float32Array(createBufferArray(this));

        this.recoil=[0,0,0];
        this.recoil_angle=0;
        this.recoil_time=5;
        this.t=0;
   }
   createBufferDataReloadSniper(gl){
     this.bufferReload_1=gl.createBuffer();
     gl.bindBuffer(gl.ARRAY_BUFFER,this.bufferReload_1);
     gl.bufferData(gl.ARRAY_BUFFER,this.arraybufferReload_1,gl.STATIC_DRAW);

     this.bufferReload_2=gl.createBuffer();
     gl.bindBuffer(gl.ARRAY_BUFFER,this.bufferReload_2);
     gl.bufferData(gl.ARRAY_BUFFER,this.arraybufferReload_2,gl.STATIC_DRAW);

     this.bufferReload_3=gl.createBuffer();
     gl.bindBuffer(gl.ARRAY_BUFFER,this.bufferReload_3);
     gl.bufferData(gl.ARRAY_BUFFER,this.arraybufferReload_3,gl.STATIC_DRAW);

     this.bufferReload_4=gl.createBuffer();
     gl.bindBuffer(gl.ARRAY_BUFFER,this.bufferReload_4);
     gl.bufferData(gl.ARRAY_BUFFER,this.arraybufferReload_4,gl.STATIC_DRAW);
   }
}

const x=0.005;
const x2=1/x;


function draw_sniper_rifle(gl,player,gun,identitymatrix,yaw,pitch,w,v,p,world,view,projection,pos,color,laserProgram){
    
    var rotation=vec3.create();
    vec3.copy(rotation,gun.gun_location);

    vec3.rotateX(rotation,rotation,vecx,deg2rad(pitch));
    vec3.rotateY(rotation,rotation,origin_vec,deg2rad(yaw));
    vec3.add(gun.translation,player.coordinate,rotation);
    

   
    if(gun.delay==0){
        gl.bindBuffer(gl.ARRAY_BUFFER,gun.buffer);
    }else{
        let ratio=gun.delay/gun.cooldown;
        if(ratio>=0.75){
            gl.bindBuffer(gl.ARRAY_BUFFER,gun.bufferReload_1);
        }else if(ratio>=0.5){
            gl.bindBuffer(gl.ARRAY_BUFFER,gun.bufferReload_2);
        }else if(ratio>=0.25){
            gl.bindBuffer(gl.ARRAY_BUFFER,gun.bufferReload_3);
        }else{
            gl.bindBuffer(gl.ARRAY_BUFFER,gun.bufferReload_4);
        }
    }

    
   
    glMatrix.mat4.translate(w,identitymatrix,gun.translation);
    
    
   
        vec3.add(gun.translation,gun.translation,gun.recoil);
        vec3.lerp(gun.recoil,gun.recoil,origin_vec,x*gun.t);
        if(gun.recoil_time>0){
            gun.recoil_time-=1;
            gun.recoil_angle+=5;
        }
        if(gun.recoil_angle>0){
            gun.recoil_angle-=1;
            if(gun.recoil_angle<0){
                gun.recoil_angle=0;
            }
        }
        if(gun.t<x2){
            gun.t+=1;
        }
        if(player.fire_state==true){
            
            glMatrix.mat4.translate(w,identitymatrix,gun.translation);
            glMatrix.mat4.rotateY(w,w,deg2rad(180+yaw));
            glMatrix.mat4.rotateX(w,w,deg2rad(360-pitch-gun.recoil_angle));
            vec3.scale(gun.recoil,player.lookingto,-7);
            gun.recoil_time=7;
            gun.t=0;
            player.fire_state=false;
            
        }else{
            glMatrix.mat4.translate(w,identitymatrix,gun.translation);
            glMatrix.mat4.rotateY(w,w,deg2rad(180+yaw));
            glMatrix.mat4.rotateX(w,w,deg2rad(360-pitch-gun.recoil_angle));
        }
       
    
   
    vec3.add(player.camera_coordinates,player.coordinate,player.lookingto)
    glMatrix.mat4.lookAt(v,player.coordinate,player.camera_coordinates,direction_up);//camera 
  
    gl.vertexAttribPointer(pos,3,gl.FLOAT,gl.FALSE,6*Float32Array.BYTES_PER_ELEMENT,0);
    gl.vertexAttribPointer(color,3,gl.FLOAT,gl.FALSE,6*Float32Array.BYTES_PER_ELEMENT,3*Float32Array.BYTES_PER_ELEMENT);
    gl.enableVertexAttribArray(pos);
    gl.enableVertexAttribArray(color);
    gl.uniformMatrix4fv(world,gl.FALSE,w);
    gl.uniformMatrix4fv(view,gl.FALSE,v);
    gl.uniformMatrix4fv(projection,gl.FALSE,p);
    gl.drawArrays(gl.TRIANGLES,0,gun.arraybuffer.length/6);
    

}






